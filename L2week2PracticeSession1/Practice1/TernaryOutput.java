public class TernaryOutput {
    public static void main(String[] args) {
        int a = 63, b = 36;

        // System.out.println(a > b ? "a is greater than b" : "b is greater than a");
        boolean x = (a < b) ? true : false;
        System.out.println(x);

        int y = (a > b) ? a : b;
        System.out.println(y);
    }
}
