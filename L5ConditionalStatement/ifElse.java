public class ifElse {
    public static void main(String[] args) {
        int age = 22;
        
        if (age >= 18) {
            System.out.println("You are adult and eligible to vote, drive.");
        } else {
            System.out.println("You are not adult enough to vote or drive.");
        }
    }
}
