public class Literals {
    public static void main(String[] args) {
        // Integer Literals
        int a = 10;
        int b = 0b1010;
        int c = 012;
        int d = 0xA;

        // Float Literals
        float e = 1.23f;
        float f = 1.23e2f;
        float g = 210E-4f;

        // Boolean Literals
        boolean h = true;
        boolean i = false;

        // Character Literals
        char j = 'A';
        char k = '\u0041';

        // String Literals
        String l = "Hello";
        String m = "World";

        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g + " " + h + " " + i + " " + j + " " + k + " " + l + " " + m);
    }
}


